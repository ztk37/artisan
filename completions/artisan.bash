                ;;
            artisan__config,set)
                cmd="artisan__config__set"
                ;;
            artisan__config__help,get)
                cmd="artisan__config__help__get"
                ;;
            artisan__config__help,help)
                cmd="artisan__config__help__help"
                ;;
            artisan__config__help,list)
                cmd="artisan__config__help__list"
                ;;
            artisan__config__help,set)
                cmd="artisan__config__help__set"
                ;;
            artisan__help,config)
                cmd="artisan__help__config"
