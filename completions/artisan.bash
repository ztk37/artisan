                ;;
            artisan__help__config,list)
                cmd="artisan__help__config__list"
                ;;
            artisan__help__config,set)
                cmd="artisan__help__config__set"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        artisan)
            opts="-h -V --help --version init new config help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        artisan__config)
            opts="-h --help list get set help"
